namespace MicroEngine.Core.Input;

/// <summary>
/// Mouse button identifiers.
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// Left mouse button (primary).
    /// </summary>
    Left = 0,

    /// <summary>
    /// Right mouse button (secondary).
    /// </summary>
    Right = 1,

    /// <summary>
    /// Middle mouse button (wheel click).
    /// </summary>
    Middle = 2,

    /// <summary>
    /// Extra mouse button 1 (side button).
    /// </summary>
    Side = 3,

    /// <summary>
    /// Extra mouse button 2 (side button).
    /// </summary>
    Extra = 4,

    /// <summary>
    /// Forward mouse button.
    /// </summary>
    Forward = 5,

    /// <summary>
    /// Back mouse button.
    /// </summary>
    Back = 6
}
